    private void jButtonLucozadeMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLucozadeMinusActionPerformed
        if (VendingMachine.drinkStock[2] > 0) { // If drink stock is greater than zero, it will decrement the stock when clicked on minus button
            VendingMachine.drinkStock[2]--;
            jTextFieldLucozadeTotal.setText("" + VendingMachine.drinkStock[2]); 
        } else {
            JOptionPane.showMessageDialog(null, "Lucozade stock cannot be negative!"); // Otherwise will tell stock cannot be negative
        }
    }//GEN-LAST:event_jButtonLucozadeMinusActionPerformed

