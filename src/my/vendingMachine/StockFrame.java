    private void jButtonPepsiMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPepsiMinusActionPerformed
        if (VendingMachine.drinkStock[3] > 0) {
            VendingMachine.drinkStock[3]--;
            jTextFieldPepsiTotal.setText("" + VendingMachine.drinkStock[3]);
        } else {
            JOptionPane.showMessageDialog(null, "Pepsi stock cannot be negative!");
        }
    }//GEN-LAST:event_jButtonPepsiMinusActionPerformed

